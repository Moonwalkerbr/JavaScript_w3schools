//    Convertes Strings em Numbers:

//  O método global 'Number()' pode converter strings em Numbers;

console.log(Number("3.14"));
//  contendo números, converte para Number;
console.log(Number(" "));
console.log(Number(""));
//  Strings vazias são convertidas em 0;
console.log(Number("23 00"));
//  Qualquer outro valor é convertido em 'NaN';
let y = "7"; //  Operador Unary
let z = y; // Converte uma variável string em number:
console.log(z);
//  se a variável não puder ser convertida, ela ainda se tornará um número, mas com o valor 'NaN' que é um number.

